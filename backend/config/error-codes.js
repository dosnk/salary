// 获取 PostgreSQL 错误的中文消息
module.exports.getPgErrorMessage = (error) => {
  if (error.code && module.exports.pgErrorMap[error.code]) {
    return module.exports.pgErrorMap[error.code].msg;
  }
  return error.message || '操作失败';
};

// 获取 PostgreSQL 错误的错误码
module.exports.getPgErrorCode = (error) => {
  if (error.code && module.exports.pgErrorMap[error.code]) {
    return module.exports.pgErrorMap[error.code].code;
  }
  return 5001;
};
